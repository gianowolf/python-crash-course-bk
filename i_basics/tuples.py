# Tuples
## A immutable list 

# Looks like a list except that use parentheses instead of square brackets
# Once defined, can be accessed just as you would for a lsit 
my_tuple = (200,40,32)
print(my_tuple)
one_element_tuple = (3,) # tuple are defined by commas 
primes_tuple = 1,3,5,7,11,13,17
print(one_element_tuple)
print(primes_tuple)

# Although you can't modify a tuple,
# you can assign a new value to a variable that represets a tuple
dimensions = 20,50
print(dimensions)
dimensions = 25,55
print(dimensions)

